#!/usr/bin/env python3
"""Benchmark tlsf-tools as a decomposition preprocessor for
synthesis, against standalone ltlsynt.

Two metrics, both rerunnable over the corpus and summarised into BENCHGRAPH.md:

  COMPLEXITY (whole corpus): how many residual clusters are classified for
  in-process routes, and what shape are the residual clusters that still need
  an external backend.

  SPEED: wall time of the wrapper pipeline (decompose + external ltlsynt per
  cluster + merge) vs a standalone `ltlsynt` baseline for the whole spec.  Solve
  STATUS is tracked too -- a fast failure is not a speedup, so speedups are only
  computed where both engines actually produced a controller.

Usage:
  scripts/benchgraph.py --corpus DIR --tlsfcompose PATH \
      --ltlsynt PATH [--out BENCHGRAPH.md] [--data data.tsv] \
      [--timeout 15] [--mem-gb 8] [--speed all|routed]
"""
import argparse
import csv
import os
import re
import shutil
import signal
import statistics
import subprocess
import sys
import tempfile
import time
from datetime import datetime, timezone

# Per-child memory cap, enforced via a systemd transient scope (cgroup) -- set
# from --mem-gb in main().  RLIMIT_AS cannot be used: OxiDD reserves large
# virtual pools (>6 GB) even for tiny games, causing spurious SIGABRT panics; and
# RLIMIT_RSS is advisory-only on Linux.  A `systemd-run --user --scope` cgroup
# with MemoryMax + MemorySwapMax=0 gives a *hard* RSS+swap cap whose breach kills
# only that child's cgroup (verified), never the whole box -- which an unenforced
# cap previously let the OS OOM killer do.
MEM_GB = 6
FORCE_NO_CGROUP = False
SENTINEL_START = "<!-- BENCHGRAPH:PREPROCESSOR START (generated by scripts/benchgraph.py) -->"
SENTINEL_END = "<!-- BENCHGRAPH:PREPROCESSOR END -->"


def systemd_run_usable():
    if shutil.which("systemd-run") is None:
        return False
    try:
        proc = subprocess.run(
            ["systemd-run", "--user", "--scope", "--quiet", "--collect",
             "--", "true"],
            stdout=subprocess.DEVNULL,
            stderr=subprocess.DEVNULL,
            timeout=5,
            check=False,
        )
    except (OSError, subprocess.TimeoutExpired):
        return False
    return proc.returncode == 0


HAVE_SYSTEMD_RUN = systemd_run_usable()


def _mem_wrap(cmd):
    """Prepend a systemd transient-scope cgroup with a hard memory cap.  --quiet
    keeps the child's stderr clean (we parse it); --collect reaps the unit even
    on failure; MemorySwapMax=0 stops the child eating (zram) swap."""
    if FORCE_NO_CGROUP or not HAVE_SYSTEMD_RUN:
        return cmd
    return ["systemd-run", "--user", "--scope", "--quiet", "--collect",
            "-p", f"MemoryMax={MEM_GB}G", "-p", "MemorySwapMax=0",
            "--"] + list(cmd)


def run_timed(cmd, timeout, env=None):
    """Run cmd with a wall-clock timeout and a hard memory cap (systemd cgroup),
    in its own session so the whole process group can be killed.  Returns
    (rc, stderr, secs, timed_out)."""
    start = time.monotonic()
    p = subprocess.Popen(_mem_wrap(cmd), stdout=subprocess.DEVNULL,
                         stderr=subprocess.PIPE,
                         env=env, start_new_session=True)
    try:
        _, err = p.communicate(timeout=timeout)
        return p.returncode, err.decode("utf-8", "replace"), time.monotonic() - start, False
    except subprocess.TimeoutExpired:
        try:
            os.killpg(os.getpgid(p.pid), signal.SIGKILL)
        except ProcessLookupError:
            pass
        try:
            _, err = p.communicate(timeout=5)
        except subprocess.TimeoutExpired:
            err = b""
        return 124, (err or b"").decode("utf-8", "replace"), time.monotonic() - start, True


def run_capture(cmd, timeout, env=None):
    """Run cmd with captured stdout/stderr, the same timeout and memory cap as
    run_timed.  Returns (rc, stdout, stderr, secs, timed_out)."""
    start = time.monotonic()
    p = subprocess.Popen(_mem_wrap(cmd), stdout=subprocess.PIPE,
                         stderr=subprocess.PIPE, env=env,
                         start_new_session=True)
    try:
        out, err = p.communicate(timeout=timeout)
        return (p.returncode, out.decode("utf-8", "replace"),
                err.decode("utf-8", "replace"), time.monotonic() - start,
                False)
    except subprocess.TimeoutExpired:
        try:
            os.killpg(os.getpgid(p.pid), signal.SIGKILL)
        except ProcessLookupError:
            pass
        try:
            out, err = p.communicate(timeout=5)
        except subprocess.TimeoutExpired:
            out, err = b"", b""
        return (124, (out or b"").decode("utf-8", "replace"),
                (err or b"").decode("utf-8", "replace"),
                time.monotonic() - start, True)


def compose_cmd(args, *parts):
    return [args.tlsfcompose, *parts, *args.compose_extra_arg]


def solve_script_path():
    return os.path.join(os.path.dirname(os.path.abspath(__file__)), "solve.sh")


def classify_residual(reasons):
    """Pick the hardest residual class from a spec's cluster failure reasons."""
    joined = " ".join(reasons)
    if re.search(r"GR\([0-9]", joined):
        return "GR(2+) generalized reactivity"
    if "liveness" in joined or "unsupported temporal" in joined:
        return "liveness (F/U/GF/Buchi)"
    if "weak-until" in joined or "release safety" in joined:
        return "W/R safety not yet handled"
    if "no usable strategy" in joined:
        return "OxiDD miss"
    return "(none / unrealizable verdict)" if reasons == [] else "other"


def classify_structured_residual(fallback_rows, reasons):
    live = []
    for row in fallback_rows:
        cls = row.get("liveness_class", "")
        if row.get("has_liveness") == "1" and cls and cls != "none":
            live.append(cls)
    if live:
        priority = [
            "nested_unsupported",
            "mixed_buchi",
            "response",
            "gf_atom_or_bool",
            "eventual",
            "until",
            "unknown",
        ]
        for cls in priority:
            if cls in live:
                return "liveness: " + cls
        return "liveness: " + sorted(live)[0]
    return classify_residual(reasons)


def parse_failure_reasons(err):
    reasons, unreal = [], 0
    for line in err.splitlines():
        if "synthesis backend failed for cluster" in line and "(" in line:
            inner = line[line.index("(") + 1:line.rindex(")")]
            reasons.append(inner.split(": ", 1)[1] if ": " in inner else inner)
        elif "is UNREALIZABLE" in line:
            unreal += 1
    return reasons, unreal


def route_row_int(row, *keys, default=0):
    for key in keys:
        val = row.get(key, "")
        if val not in ("", "-", None):
            return int(val)
    return default


def route_row_uses_oxidd(row):
    val = row.get("uses_oxidd")
    if val in ("0", "1"):
        return val == "1"
    return row.get("backend", "").startswith("OxiDD")


def route_row_exact(row):
    return row.get("exact") == "1"


def parse_route_stats(text):
    lines = [line for line in text.splitlines() if line.strip()]
    if not lines:
        return []
    return list(csv.DictReader(lines, delimiter="\t"))


def probe(spec, args):
    """Route diagnostics: which clusters match in-process route recognizers."""
    stats_cmd = compose_cmd(args, "--split", "--route-stats")
    stats_cmd.append(spec)
    s_rc, s_out, s_err, _, s_to = run_capture(stats_cmd, args.timeout)
    if s_to or s_rc != 0:
        return {
            "self_contained": False,
            "abs_clusters": 0,
            "residual_class": classify_residual([s_err.strip()] if s_err.strip() else []),
            "probe_unreal": 0,
            "full_nodes": 0,
            "residual_nodes": 0,
            "full_bytes": 0,
            "residual_bytes": 0,
            "max_outputs_any_cluster": 0,
            "max_outputs_fallback": 0,
            "n_oxidd": 0,
            "n_ltlsynt": 0,
        }

    rows = parse_route_stats(s_out)
    fallback_rows = [r for r in rows if not route_row_uses_oxidd(r)]
    oxidd_rows = [r for r in rows if route_row_uses_oxidd(r)]
    reasons = [r.get("reason", "") for r in fallback_rows if r.get("reason", "")]

    full_nodes = sum(route_row_int(r, "formula_nodes", "nodes") for r in rows)
    residual_nodes = sum(
        route_row_int(r, "formula_nodes", "nodes") for r in fallback_rows
    )
    full_bytes = sum(route_row_int(r, "formula_bytes") for r in rows)
    residual_bytes = sum(route_row_int(r, "formula_bytes") for r in fallback_rows)
    outputs = [route_row_int(r, "n_outputs", "n_outs") for r in rows]
    fallback_outputs = [
        route_row_int(r, "n_outputs", "n_outs") for r in fallback_rows
    ]

    return {
        "self_contained": len(fallback_rows) == 0,
        "abs_clusters": len(oxidd_rows),
        "residual_class": classify_structured_residual(fallback_rows, reasons),
        "probe_unreal": 0,
        "full_nodes": full_nodes,
        "residual_nodes": residual_nodes,
        "full_bytes": full_bytes,
        "residual_bytes": residual_bytes,
        "max_outputs_any_cluster": max(outputs) if outputs else 0,
        "max_outputs_fallback": max(fallback_outputs) if fallback_outputs else 0,
        "n_oxidd": len(oxidd_rows),
        "n_ltlsynt": len(fallback_rows),
    }


def ours_status(spec, args):
    env = dict(os.environ)
    with tempfile.TemporaryDirectory() as td:
        cmd = [
            solve_script_path(),
            "--tlsfcompose", args.tlsfcompose,
            "--solver", args.ltlsynt,
            "--output", os.path.join(td, "strategy.aag"),
            spec,
        ]
        rc, err, secs, to = run_timed(cmd, args.timeout, env)
    if to:
        st = "TIMEOUT"
    elif rc == 0:
        st = "SOLVED"
    elif "is UNREALIZABLE" in err:
        st = "UNREAL"
    else:
        st = "FAILED"
    return st, secs


def sibling_tool(path, name):
    d = os.path.dirname(path)
    return os.path.join(d, name) if d else name


def absolutize_tool(path):
    if path and os.sep in path and not os.path.isabs(path):
        return os.path.abspath(path)
    return path


def parse_basic_signals(text):
    ins, outs, section = [], [], None
    for line in text.splitlines():
        s = line.split("//", 1)[0].strip()
        if s == "INPUTS {":
            section = "inputs"
            continue
        if s == "OUTPUTS {":
            section = "outputs"
            continue
        if section and s == "}":
            section = None
            continue
        if section and s.endswith(";"):
            name = s[:-1].strip()
            if name:
                (ins if section == "inputs" else outs).append(name)
    return ins, outs


def base_status_via_tlsf_tools(spec, args):
    start = time.monotonic()

    def remaining():
        return max(0.0, args.timeout - (time.monotonic() - start))

    try:
        basic = subprocess.run(
            [args.tlsf2tlsf, "--basic", spec],
            stdout=subprocess.PIPE,
            stderr=subprocess.PIPE,
            text=True,
            timeout=remaining(),
        )
    except subprocess.TimeoutExpired:
        return "TIMEOUT", time.monotonic() - start
    if basic.returncode != 0:
        return "ERROR", time.monotonic() - start
    ins, outs = parse_basic_signals(basic.stdout)
    if not outs:
        return "ERROR", time.monotonic() - start

    try:
        formula = subprocess.run(
            [args.tlsf2ltl, "--format", "ltlxba", spec],
            stdout=subprocess.PIPE,
            stderr=subprocess.PIPE,
            text=True,
            timeout=remaining(),
        )
    except subprocess.TimeoutExpired:
        return "TIMEOUT", time.monotonic() - start
    if formula.returncode != 0 or not formula.stdout.strip():
        return "ERROR", time.monotonic() - start

    tmp = None
    try:
        with tempfile.NamedTemporaryFile("w", suffix=".ltl", delete=False) as f:
            tmp = f.name
            f.write(formula.stdout.strip())
            f.write("\n")
        rem = remaining()
        if rem <= 0:
            return "TIMEOUT", time.monotonic() - start
        ins = [name.lower() for name in ins]
        outs = [name.lower() for name in outs]
        cmd = [args.ltlsynt, "-F", tmp, "--outs", ",".join(outs), "--aiger"]
        if ins:
            cmd += ["--ins", ",".join(ins)]
        rc, _, _, to = run_timed(cmd, rem)
    finally:
        if tmp:
            try:
                os.unlink(tmp)
            except OSError:
                pass
    secs = time.monotonic() - start
    if to:
        st = "TIMEOUT"
    elif rc == 0:
        st = "SOLVED"
    elif rc == 1:
        st = "UNREAL"
    else:
        st = "ERROR"
    return st, secs


def base_status(spec, args):
    # Baseline: standalone ltlsynt fed our own TLSF->LTL translation (syfco-free).
    return base_status_via_tlsf_tools(spec, args)


def baseline_description(args):
    return ("`tlsf2tlsf --basic` + `tlsf2ltl --format ltlxba` + "
            "`ltlsynt -F … --ins … --outs … --aiger`")


def load_rows(path):
    rows = []
    with open(path) as f:
        next(f)
        for line in f:
            p = line.rstrip("\n").split("\t")
            r = {
                "name": p[0], "self_contained": p[1] == "1", "abs_clusters": int(p[2]),
                "residual_class": p[3], "ours_st": p[4],
                "ours_t": float(p[5]) if p[5] else 0.0, "base_st": p[6],
                "base_t": float(p[7]) if p[7] else 0.0,
            }
            # Residual-reduction columns (added later; tolerate older TSVs).
            r["full_nodes"] = int(p[8]) if len(p) > 8 and p[8] else 0
            r["residual_nodes"] = int(p[9]) if len(p) > 9 and p[9] else 0
            r["n_oxidd"] = int(p[10]) if len(p) > 10 and p[10] else 0
            r["n_ltlsynt"] = int(p[11]) if len(p) > 11 and p[11] else 0
            r["full_bytes"] = int(p[12]) if len(p) > 12 and p[12] else 0
            r["residual_bytes"] = int(p[13]) if len(p) > 13 and p[13] else 0
            r["max_outputs_any_cluster"] = int(p[14]) if len(p) > 14 and p[14] else 0
            r["max_outputs_fallback"] = int(p[15]) if len(p) > 15 and p[15] else 0
            rows.append(r)
    return rows


def write_rows(path, rows):
    with open(path, "w") as f:
        f.write("name\tself_contained\tabs_clusters\tresidual_class\t"
                "ours_st\tours_t\tbase_st\tbase_t\t"
                "full_nodes\tresidual_nodes\tn_oxidd\tn_ltlsynt\t"
                "full_bytes\tresidual_bytes\tmax_outputs_any_cluster\t"
                "max_outputs_fallback\n")
        for r in rows:
            f.write(f"{r['name']}\t{int(r['self_contained'])}\t"
                    f"{r['abs_clusters']}\t{r['residual_class']}\t"
                    f"{r['ours_st']}\t{r['ours_t'] or ''}\t"
                    f"{r['base_st']}\t{r['base_t'] or ''}\t"
                    f"{r['full_nodes']}\t{r['residual_nodes']}\t"
                    f"{r['n_oxidd']}\t{r['n_ltlsynt']}\t"
                    f"{r.get('full_bytes', 0)}\t"
                    f"{r.get('residual_bytes', 0)}\t"
                    f"{r.get('max_outputs_any_cluster', 0)}\t"
                    f"{r.get('max_outputs_fallback', 0)}\n")


def main():
    ap = argparse.ArgumentParser()
    ap.add_argument("--corpus", required=True)
    ap.add_argument("--tlsfcompose", required=True)
    ap.add_argument("--ltlsynt", default=shutil.which("ltlsynt") or "ltlsynt")
    ap.add_argument("--tlsf2ltl", default=None,
                    help="tlsf2ltl path for the ltlsynt baseline translation")
    ap.add_argument("--tlsf2tlsf", default=None,
                    help="tlsf2tlsf path for the ltlsynt baseline translation")
    ap.add_argument("--baseline-mode", choices=["auto", "tlsf-tools"],
                    default="auto",
                    help="baseline translation path for standalone ltlsynt "
                         "(tlsf-tools only)")
    ap.add_argument("--out", default="BENCHGRAPH.md")
    ap.add_argument("--data", default=None, help="per-spec TSV (default: alongside --out)")
    ap.add_argument("--timeout", type=int, default=15)
    ap.add_argument("--mem-gb", type=int, default=6,
                    help="hard per-run memory cap in GB (systemd cgroup); breach "
                         "kills only that child, not the box")
    ap.add_argument("--allow-uncapped", action="store_true",
                    help="run without the systemd memory cgroup when unavailable; "
                         "intended only for small smoke runs")
    ap.add_argument("--speed", choices=["routed", "all"], default="routed",
                    help="which specs to time (default: only route-contributing)")
    ap.add_argument("--compose-extra-arg", action="append", default=[],
                    help="extra argument passed to every tlsfcompose invocation; repeatable")
    ap.add_argument("--limit", type=int, default=0, help="cap #specs (0 = all; for smoke tests)")
    ap.add_argument("--from-data", default=None,
                    help="regenerate the report from an existing TSV (skip the corpus run)")
    ap.add_argument("--resume", action="store_true",
                    help="skip specs already present in --data (recover a crashed run)")
    ap.add_argument("--refresh-baseline-errors", action="store_true",
                    help="recompute baseline columns for timed rows whose baseline status is ERROR")
    args = ap.parse_args()
    args.tlsfcompose = absolutize_tool(args.tlsfcompose)
    args.ltlsynt = absolutize_tool(args.ltlsynt)
    data_path = args.data or (os.path.splitext(args.out)[0] + ".tsv")
    if args.tlsf2ltl is None:
        args.tlsf2ltl = sibling_tool(args.tlsfcompose, "tlsf2ltl")
    if args.tlsf2tlsf is None:
        args.tlsf2tlsf = sibling_tool(args.tlsfcompose, "tlsf2tlsf")
    args.tlsf2ltl = absolutize_tool(args.tlsf2ltl)
    args.tlsf2tlsf = absolutize_tool(args.tlsf2tlsf)
    if args.baseline_mode == "auto":
        args.baseline_mode = "tlsf-tools"

    global MEM_GB, FORCE_NO_CGROUP
    MEM_GB = args.mem_gb
    FORCE_NO_CGROUP = args.allow_uncapped

    if args.refresh_baseline_errors:
        rows = load_rows(data_path)
        for i, r in enumerate(rows, 1):
            if r["ours_st"] and r["base_st"] == "ERROR":
                spec = os.path.join(args.corpus, r["name"])
                r["base_st"], r["base_t"] = base_status(spec, args)
            if i % 50 == 0 or i == len(rows):
                print(f"[{i}/{len(rows)}] baseline refresh", file=sys.stderr, flush=True)
        write_rows(data_path, rows)
        write_report(args, rows, len(rows), data_path)
        print(f"refreshed baseline columns in {data_path} and {args.out}",
              file=sys.stderr)
        return

    if args.from_data:
        rows = load_rows(args.from_data)
        write_report(args, rows, len(rows), args.from_data)
        print(f"regenerated {args.out} from {args.from_data}", file=sys.stderr)
        return

    if not HAVE_SYSTEMD_RUN and not FORCE_NO_CGROUP:
        sys.exit("error: systemd-run is not available/usable; a corpus run "
                 "would be UNPROTECTED against OOM.  Install/configure "
                 "systemd-run, use --from-data to re-render, or pass "
                 "--allow-uncapped for a small smoke run.")

    specs = []
    for root, _, files in os.walk(args.corpus):
        for f in files:
            if f.endswith(".tlsf"):
                specs.append(os.path.join(root, f))
    specs.sort()
    if args.limit:
        specs = specs[:args.limit]
    total = len(specs)

    # Rows are written incrementally (append + flush) so a crash mid-run -- e.g.
    # the OOM killer on a memory-hungry spec -- loses nothing and `--resume`
    # continues.
    header = ("name\tself_contained\tabs_clusters\tresidual_class\t"
              "ours_st\tours_t\tbase_st\tbase_t\t"
              "full_nodes\tresidual_nodes\tn_oxidd\tn_ltlsynt\t"
              "full_bytes\tresidual_bytes\tmax_outputs_any_cluster\t"
              "max_outputs_fallback\n")
    done = set()
    if args.resume and os.path.exists(data_path):
        done = {r["name"] for r in load_rows(data_path)}
        f = open(data_path, "a")
    else:
        f = open(data_path, "w")
        f.write(header)
    f.flush()

    t0 = time.monotonic()
    for i, spec in enumerate(specs, 1):
        name = os.path.relpath(spec, args.corpus)
        if name in done:
            continue
        pr = probe(spec, args)
        do_speed = args.speed == "all" or pr["abs_clusters"] > 0
        ours = base = ("", "")
        if do_speed:
            ours = ours_status(spec, args)
            base = base_status(spec, args)
        f.write(f"{name}\t{int(pr['self_contained'])}\t{pr['abs_clusters']}\t"
                f"{pr['residual_class']}\t{ours[0]}\t{ours[1] or ''}\t"
                f"{base[0]}\t{base[1] or ''}\t"
                f"{pr['full_nodes']}\t{pr['residual_nodes']}\t"
                f"{pr['n_oxidd']}\t{pr['n_ltlsynt']}\t"
                f"{pr['full_bytes']}\t{pr['residual_bytes']}\t"
                f"{pr['max_outputs_any_cluster']}\t"
                f"{pr['max_outputs_fallback']}\n")
        f.flush()
        if i % 50 == 0 or i == total:
            el = time.monotonic() - t0
            print(f"[{i}/{total}] {el:.0f}s elapsed", file=sys.stderr, flush=True)
    f.close()

    rows = load_rows(data_path)
    write_report(args, rows, total, data_path)
    print(f"wrote {args.out} and {data_path}", file=sys.stderr)


def write_survival_plot(rows, png_path, timeout):
    """Cactus/survival plot: for each engine, the i-th fastest definitive solve.
    A curve further RIGHT solves more instances; LOWER means faster.  "Solved" =
    produced a controller (SOLVED) or a verdict (UNREAL) within the timeout."""
    try:
        import matplotlib
        matplotlib.use("Agg")
        import matplotlib.pyplot as plt
    except Exception:
        return False

    def solved_times(st_key, t_key):
        return sorted(r[t_key] for r in rows
                      if r[st_key] in ("SOLVED", "UNREAL") and r[t_key] > 0)

    base = solved_times("base_st", "base_t")
    ours = solved_times("ours_st", "ours_t")
    if not base and not ours:
        return False
    fig, ax = plt.subplots(figsize=(7.2, 4.4))
    ax.step(range(1, len(base) + 1), base, where="post", color="#c44e52",
            lw=1.8, label=f"ltlsynt ({len(base)} solved)")
    ax.step(range(1, len(ours) + 1), ours, where="post", color="#4c72b0",
            lw=1.8, label=f"preprocessor + ltlsynt ({len(ours)} solved)")
    ax.axhline(timeout, color="k", lw=0.8, ls="--")
    ax.set_yscale("log")
    ax.set_xlabel("instances solved (cumulative)")
    ax.set_ylabel("time per instance (s, log)")
    ax.set_title("Survival: ltlsynt vs preprocessor + ltlsynt")
    ax.grid(True, which="both", ls=":", lw=0.4, alpha=0.5)
    ax.legend(loc="upper left", fontsize=9)
    fig.tight_layout()
    os.makedirs(os.path.dirname(png_path), exist_ok=True)
    fig.savefig(png_path, dpi=120)
    plt.close(fig)
    return True


def write_report(args, rows, total, data_path):
    from collections import Counter
    sc = [r for r in rows if r["self_contained"]]
    timed = [r for r in rows if r["ours_st"]]

    def ok(st):
        return st in ("SOLVED", "UNREAL")  # definitive answer within timeout

    both = [r for r in timed if ok(r["ours_st"]) and ok(r["base_st"])]
    only_ours = [r for r in timed if ok(r["ours_st"]) and not ok(r["base_st"])]
    only_base = [r for r in timed if ok(r["base_st"]) and not ok(r["ours_st"])]
    neither = [r for r in timed if not ok(r["ours_st"]) and not ok(r["base_st"])]
    n_ours = sum(1 for r in timed if ok(r["ours_st"]))
    n_base = sum(1 for r in timed if ok(r["base_st"]))
    # Wall-time speedup where BOTH produced a controller (fair comparison).
    both_ctrl = [r for r in timed if r["ours_st"] == "SOLVED"
                 and r["base_st"] == "SOLVED" and r["ours_t"] > 1e-6]
    speedups = [r["base_t"] / r["ours_t"] for r in both_ctrl]
    sum_ours = sum(r["ours_t"] for r in both_ctrl)
    sum_base = sum(r["base_t"] for r in both_ctrl)
    # Realizability disagreements (ltlsynt is authoritative).
    false_unreal = [r for r in timed
                    if r["ours_st"] == "UNREAL" and r["base_st"] == "SOLVED"]
    false_real = [r for r in timed
                  if r["ours_st"] == "SOLVED" and r["base_st"] == "UNREAL"]
    win_fam = Counter(os.path.dirname(r["name"]) or "." for r in only_ours)

    def fam_summary(counter):
        parts = [f"{d.split('/')[-1]}×{n}" for d, n in counter.most_common()
                 if d != "."]
        return ", ".join(parts[:6])

    png_rel = "docs/benchgraph/survival.png"
    png_abs = os.path.join(os.path.dirname(os.path.abspath(args.out)) or ".",
                           png_rel)
    have_plot = write_survival_plot(timed, png_abs, args.timeout)

    commit = subprocess.run(["git", "rev-parse", "--short", "HEAD"],
                            capture_output=True, text=True).stdout.strip()
    now = datetime.now(timezone.utc).strftime("%Y-%m-%d %H:%M UTC")

    def med(xs):
        return statistics.median(xs) if xs else float("nan")

    def geo(xs):
        return statistics.geometric_mean(xs) if xs else float("nan")

    cap_how = ("UNENFORCED (--allow-uncapped)" if FORCE_NO_CGROUP else
               "systemd cgroup hard cap" if HAVE_SYSTEMD_RUN else
               "UNENFORCED (no usable systemd-run)")

    L = []
    L.append(SENTINEL_START + "\n")
    L.append("## ltlsynt vs preprocessor + ltlsynt (`scripts/benchgraph.py`)\n")
    L.append("Head-to-head synthesis: standalone **ltlsynt** vs the **preprocessor** "
             "(`--split` decomposition + external cluster solving + merge) that "
             "forwards exact residual clusters to ltlsynt. Each (engine, spec) pair "
             "gets the same timeout. Regenerate with `scripts/benchgraph.py` "
             "(or `--from-data benchgraph.tsv` to re-render).\n")
    L.append(f"\n### Run: {now} · commit `{commit}`\n")
    L.append(f"- Corpus `{args.corpus}` ({total} specs); timeout **{args.timeout}s**, "
             f"{MEM_GB} GB/run ({cap_how}), sequential.\n"
             f"- ltlsynt: {baseline_description(args)}\n"
             f"- preprocessor + ltlsynt: `scripts/solve.sh --solver …`"
             + (f" {' '.join(args.compose_extra_arg)}" if args.compose_extra_arg else "")
             + f"\n- Specs with no fallback-classified residual clusters: "
             f"**{len(sc)}/{total} = {100*len(sc)/total:.1f}%**.\n")

    L.append("\n### Solved within timeout\n")
    L.append(f"| engine | solved | only this engine |\n|---|--:|--:|\n")
    L.append(f"| ltlsynt | {n_base}/{len(timed)} | {len(only_base)} |\n")
    L.append(f"| preprocessor + ltlsynt | {n_ours}/{len(timed)} | {len(only_ours)} |\n")
    net = n_ours - n_base
    L.append(f"\n- Both: {len(both)}; neither: {len(neither)}. "
             f"**Net gain from the preprocessor: {net:+d}** "
             f"({len(only_ours)} won, {len(only_base)} lost).\n")
    if win_fam:
        L.append(f"- Preprocessor-only solves by family: {fam_summary(win_fam)}.\n")

    if speedups:
        L.append("\n### Speed (both produced a controller)\n")
        L.append(f"- {len(both_ctrl)} specs both SOLVED. "
                 f"**Speedup `ltlsynt/preproc`: median ×{med(speedups):.2f}, "
                 f"geomean ×{geo(speedups):.2f}** "
                 f"(faster {sum(1 for s in speedups if s>1)}, "
                 f"slower {sum(1 for s in speedups if s<1)}); "
                 f"aggregate wall ×{sum_base/sum_ours:.2f}.\n")

    if have_plot:
        L.append(f"\n![Survival: ltlsynt vs preprocessor + ltlsynt]({png_rel})\n")

    if false_unreal or false_real:
        L.append(f"\n- Correctness check (ltlsynt authoritative): "
                 f"{len(false_unreal)} false-UNREAL, {len(false_real)} false-REAL "
                 f"from the preprocessor.\n")
    L.append(SENTINEL_END + "\n")
    section = "".join(L)

    # Inject as a self-delimited section: replace between the sentinels if they
    # already exist (so reruns only touch this section), else append to the doc.
    existing = ""
    if os.path.exists(args.out):
        with open(args.out) as f:
            existing = f.read()
    if SENTINEL_START in existing and SENTINEL_END in existing:
        pre = existing[:existing.index(SENTINEL_START)]
        post = existing[existing.index(SENTINEL_END) + len(SENTINEL_END):]
        new = pre + section.rstrip("\n") + post
    elif existing.strip():
        new = existing.rstrip("\n") + "\n\n" + section
    else:
        new = "# BENCHGRAPH\n\n" + section
    with open(args.out, "w") as f:
        f.write(new)


if __name__ == "__main__":
    main()
