#!/usr/bin/env python3
"""Benchmark tlsf-tools (templates + OxiDD) as a FAST preprocessor for
synthesis, against standalone ltlsynt.

Two metrics, both rerunnable over the corpus and summarised into BENCHGRAPH.md:

  COMPLEXITY (whole corpus, ltlsynt disabled): how many specs are fully
  self-contained via templates+OxiDD, and what shape are the residual
  clusters that still need a liveness backend.

  SPEED (focus: specs where OxiDD handles >=1 cluster): wall time of the full
  tlsf-tools pipeline (templates + OxiDD + ltlsynt on residuals) vs standalone
  `ltlsynt --tlsf` on the whole spec.  Solve STATUS is tracked too -- a fast
  failure is not a speedup, so speedups are only computed where both engines
  actually produced a controller.

Usage:
  scripts/benchgraph.py --corpus DIR --tlsfcompose PATH \
      --ltlsynt PATH [--out BENCHGRAPH.md] [--data data.tsv] \
      [--timeout 15] [--mem-gb 8] [--speed all|oxidd]
"""
import argparse
import os
import resource
import shutil
import signal
import statistics
import subprocess
import sys
import time
from datetime import datetime, timezone

MEM_BYTES = 6 * 10**9  # per-child cap (physical RSS); keep headroom so the box does not OOM
# RLIMIT_AS (virtual address space) cannot be used here: OxiDD reserves large
# virtual pools (>6 GB) even for tiny games, causing spurious SIGABRT panics.
# RLIMIT_RSS is advisory-only on Linux; we rely on the OS OOM killer for protection.
SENTINEL_START = "<!-- BENCHGRAPH:PREPROCESSOR START (generated by scripts/benchgraph.py) -->"
SENTINEL_END = "<!-- BENCHGRAPH:PREPROCESSOR END -->"


def _limits():
    pass  # virtual-address cap removed; see MEM_BYTES comment above


def run_timed(cmd, timeout, env=None):
    """Run cmd with a wall-clock timeout and a memory cap, in its own session so
    the whole process group can be killed.  Returns (rc, stderr, secs, timed_out)."""
    start = time.monotonic()
    p = subprocess.Popen(cmd, stdout=subprocess.DEVNULL, stderr=subprocess.PIPE,
                         env=env, preexec_fn=_limits, start_new_session=True)
    try:
        _, err = p.communicate(timeout=timeout)
        return p.returncode, err.decode("utf-8", "replace"), time.monotonic() - start, False
    except subprocess.TimeoutExpired:
        try:
            os.killpg(os.getpgid(p.pid), signal.SIGKILL)
        except ProcessLookupError:
            pass
        try:
            _, err = p.communicate(timeout=5)
        except subprocess.TimeoutExpired:
            err = b""
        return 124, (err or b"").decode("utf-8", "replace"), time.monotonic() - start, True


def classify_residual(reasons):
    """Pick the hardest residual class from a spec's cluster failure reasons."""
    joined = " ".join(reasons)
    import re
    if re.search(r"GR\([0-9]", joined):
        return "GR(2+) generalized reactivity"
    if "liveness" in joined or "unsupported temporal" in joined:
        return "liveness (F/U/GF/Buchi)"
    if "weak-until" in joined or "release safety" in joined:
        return "W/R safety not yet handled"
    if "no usable strategy" in joined:
        return "OxiDD miss"
    return "(none / unrealizable verdict)" if reasons == [] else "other"


def probe(spec, args):
    """ltlsynt-disabled run: self-contained? which OxiDD backends? residual shape."""
    env = dict(os.environ, TLSFCOMPOSE_DEBUG="1")
    cmd = [args.tlsfcompose, "--split", "--aiger",
           "--ltlsynt", "/bin/false", spec]
    rc, err, _, _ = run_timed(cmd, args.timeout, env)
    routes, reasons, unreal = [], [], 0
    for line in err.splitlines():
        if "routed to " in line:
            routes.append(line.split("routed to ", 1)[1])
        elif "synthesis backend failed for cluster" in line and "(" in line:
            inner = line[line.index("(") + 1:line.rindex(")")]
            reasons.append(inner.split(": ", 1)[1] if ": " in inner else inner)
        elif "is UNREALIZABLE" in line:
            unreal += 1
    oxidd_clusters = sum(1 for r in routes if r.startswith("OxiDD"))
    return {
        "self_contained": rc == 0,
        "abs_clusters": oxidd_clusters,
        "residual_class": classify_residual(reasons),
        "probe_unreal": unreal,
    }


def ours_status(spec, args):
    env = dict(os.environ)
    cmd = [args.tlsfcompose, "--split", "--aiger",
           "--ltlsynt", args.ltlsynt, spec]
    rc, err, secs, to = run_timed(cmd, args.timeout, env)
    if to:
        st = "TIMEOUT"
    elif rc == 0:
        st = "SOLVED"
    elif "is UNREALIZABLE" in err:
        st = "UNREAL"
    else:
        st = "FAILED"
    return st, secs


def base_status(spec, args):
    rc, _, secs, to = run_timed([args.ltlsynt, "--tlsf=" + spec, "--aiger"], args.timeout)
    if to:
        st = "TIMEOUT"
    elif rc == 0:
        st = "SOLVED"
    elif rc == 1:
        st = "UNREAL"
    else:
        st = "ERROR"
    return st, secs


def load_rows(path):
    rows = []
    with open(path) as f:
        next(f)
        for line in f:
            p = line.rstrip("\n").split("\t")
            rows.append({
                "name": p[0], "self_contained": p[1] == "1", "abs_clusters": int(p[2]),
                "residual_class": p[3], "ours_st": p[4],
                "ours_t": float(p[5]) if p[5] else 0.0, "base_st": p[6],
                "base_t": float(p[7]) if p[7] else 0.0,
            })
    return rows


def main():
    ap = argparse.ArgumentParser()
    ap.add_argument("--corpus", required=True)
    ap.add_argument("--tlsfcompose", required=True)
    ap.add_argument("--ltlsynt", default=shutil.which("ltlsynt") or "ltlsynt")
    ap.add_argument("--out", default="BENCHGRAPH.md")
    ap.add_argument("--data", default=None, help="per-spec TSV (default: alongside --out)")
    ap.add_argument("--timeout", type=int, default=15)
    ap.add_argument("--speed", choices=["oxidd", "all"], default="oxidd",
                    help="which specs to time (default: only OxiDD-contributing)")
    ap.add_argument("--limit", type=int, default=0, help="cap #specs (0 = all; for smoke tests)")
    ap.add_argument("--from-data", default=None,
                    help="regenerate the report from an existing TSV (skip the corpus run)")
    ap.add_argument("--resume", action="store_true",
                    help="skip specs already present in --data (recover a crashed run)")
    args = ap.parse_args()
    data_path = args.data or (os.path.splitext(args.out)[0] + ".tsv")

    if args.from_data:
        rows = load_rows(args.from_data)
        write_report(args, rows, len(rows), args.from_data)
        print(f"regenerated {args.out} from {args.from_data}", file=sys.stderr)
        return

    specs = []
    for root, _, files in os.walk(args.corpus):
        for f in files:
            if f.endswith(".tlsf"):
                specs.append(os.path.join(root, f))
    specs.sort()
    if args.limit:
        specs = specs[:args.limit]
    total = len(specs)

    # Rows are written incrementally (append + flush) so a crash mid-run -- e.g.
    # the OOM killer on a memory-hungry spec -- loses nothing and `--resume`
    # continues.
    header = ("name\tself_contained\tabs_clusters\tresidual_class\t"
              "ours_st\tours_t\tbase_st\tbase_t\n")
    done = set()
    if args.resume and os.path.exists(data_path):
        done = {r["name"] for r in load_rows(data_path)}
        f = open(data_path, "a")
    else:
        f = open(data_path, "w")
        f.write(header)
    f.flush()

    t0 = time.monotonic()
    for i, spec in enumerate(specs, 1):
        name = os.path.relpath(spec, args.corpus)
        if name in done:
            continue
        pr = probe(spec, args)
        do_speed = args.speed == "all" or pr["abs_clusters"] > 0
        ours = base = ("", "")
        if do_speed:
            ours = ours_status(spec, args)
            base = base_status(spec, args)
        f.write(f"{name}\t{int(pr['self_contained'])}\t{pr['abs_clusters']}\t"
                f"{pr['residual_class']}\t{ours[0]}\t{ours[1] or ''}\t"
                f"{base[0]}\t{base[1] or ''}\n")
        f.flush()
        if i % 50 == 0 or i == total:
            el = time.monotonic() - t0
            print(f"[{i}/{total}] {el:.0f}s elapsed", file=sys.stderr, flush=True)
    f.close()

    rows = load_rows(data_path)
    write_report(args, rows, total, data_path)
    print(f"wrote {args.out} and {data_path}", file=sys.stderr)


def write_report(args, rows, total, data_path):
    sc = [r for r in rows if r["self_contained"]]
    sc_abs = [r for r in sc if r["abs_clusters"] > 0]
    reach = [r for r in rows if r["abs_clusters"] > 0]
    # complexity: residual class among non-self-contained
    from collections import Counter
    resid = Counter(r["residual_class"] for r in rows if not r["self_contained"])

    # speed: only specs we actually timed
    timed = [r for r in rows if r["ours_st"]]
    both = [r for r in timed if r["ours_st"] == "SOLVED" and r["base_st"] == "SOLVED"]
    speedups = [r["base_t"] / r["ours_t"] for r in both if r["ours_t"] > 1e-6]
    ours_win_base_to = [r for r in timed if r["ours_st"] == "SOLVED" and r["base_st"] == "TIMEOUT"]
    gap = [r for r in timed if r["ours_st"] == "FAILED" and r["base_st"] == "SOLVED"]
    ours_to = [r for r in timed if r["ours_st"] == "TIMEOUT" and r["base_st"] == "SOLVED"]
    false_unreal = [r for r in timed if r["ours_st"] == "UNREAL" and r["base_st"] == "SOLVED"]
    # the honest completeness deficit: ltlsynt produced a controller, we did not
    less_complete = gap + false_unreal + ours_to
    sum_ours = sum(r["ours_t"] for r in both)
    sum_base = sum(r["base_t"] for r in both)

    commit = subprocess.run(["git", "rev-parse", "--short", "HEAD"],
                            capture_output=True, text=True).stdout.strip()
    now = datetime.now(timezone.utc).strftime("%Y-%m-%d %H:%M UTC")

    def med(xs):
        return statistics.median(xs) if xs else float("nan")

    def geo(xs):
        return statistics.geometric_mean(xs) if xs else float("nan")

    L = []
    L.append(SENTINEL_START + "\n")
    L.append("## Preprocessor speed & complexity vs ltlsynt (`scripts/benchgraph.py`)\n")
    L.append("Is templates+OxiDD a FAST preprocessor? Two metrics: residual **complexity**\n"
             "(what's left after templates+OxiDD) and **speed** (our full pipeline vs standalone\n"
             "`ltlsynt --tlsf` on the whole spec). Goal: carve off safety with OxiDD, forward\n"
             "only the hard liveness residual to ltlsynt, and never be slower or less complete.\n"
             "Regenerate: `scripts/benchgraph.py --corpus DIR --tlsfcompose … --ltlsynt …`\n"
             "(or `--from-data benchgraph.tsv` to re-render this section without re-running).\n")
    L.append(f"\n### Run: {now} · commit `{commit}`\n")
    L.append(f"- Corpus: `{args.corpus}` ({total} specs)\n"
             f"- Caps: timeout {args.timeout}s/run, "
             f"{MEM_BYTES//10**9} GB RAM, sequential\n"
             f"- Baseline: `ltlsynt --tlsf=SPEC --aiger` (syfco translation, full synthesis)\n"
             f"- Ours: `tlsfcompose --split --aiger --ltlsynt …`\n"
             f"- Per-spec data: `{os.path.basename(data_path)}`\n")

    L.append("\n### Complexity\n")
    L.append(f"- **Self-contained (templates+OxiDD, no ltlsynt): {len(sc)}/{total} "
             f"= {100*len(sc)/total:.1f}%** ({len(sc_abs)} use OxiDD).\n")
    L.append(f"- **OxiDD reach (≥1 cluster): {len(reach)}/{total} = {100*len(reach)/total:.1f}%**.\n")
    L.append("- Residual shape (specs not self-contained), hardest cluster:\n\n")
    L.append("  | residual class | specs |\n  |---|---|\n")
    for cls, n in resid.most_common():
        L.append(f"  | {cls} | {n} |\n")

    med_ours = med([r["ours_t"] for r in both])
    med_base = med([r["base_t"] for r in both])
    from collections import Counter
    win_fam = Counter(os.path.dirname(r["name"]) or "." for r in ours_win_base_to)
    gap_fam = Counter(os.path.dirname(r["name"]) or "." for r in gap)

    L.append("\n### Speed (OxiDD-contributing specs)\n")
    L.append(f"- Timed: {len(timed)} specs. Both produced a controller: {len(both)}.\n")
    if speedups:
        L.append(f"- **Both-solved speedup `base/ours`: median ×{med(speedups):.2f}, "
                 f"geomean ×{geo(speedups):.2f}** "
                 f"(faster: {sum(1 for s in speedups if s>1)}, slower: {sum(1 for s in speedups if s<1)}).\n")
        L.append(f"- Absolute wall on both-solved: **median ours {med_ours*1000:.0f} ms vs base "
                 f"{med_base*1000:.0f} ms** (near parity); mean ours {sum_ours/len(both)*1000:.0f} ms "
                 f"vs base {sum_base/len(both)*1000:.0f} ms.\n")
        L.append(f"- Total wall on both-solved: ours {sum_ours:.1f}s vs base {sum_base:.1f}s "
                 f"(**×{sum_base/sum_ours:.2f}** aggregate).\n")
    L.append(f"- Ours solves where **base times out** (≥{args.timeout}s): {len(ours_win_base_to)} clear wins"
             + (" — " + ", ".join(f"{d.split('/')[-1] or d}×{n}" for d, n in win_fam.most_common()) if win_fam else "")
             + ".\n")

    fu_fam = Counter(os.path.dirname(r["name"]) or "." for r in false_unreal)
    L.append("\n### Completeness vs ltlsynt\n")
    L.append(f"- **ltlsynt produced a controller but we did not: {len(less_complete)}** — the honest "
             f"deficit (we are *less complete* on these). Breakdown: {len(false_unreal)} we wrongly call "
             f"**UNREALIZABLE**, {len(gap)} backend **FAILED**, {len(ours_to)} **timed out**.\n")
    if fu_fam:
        L.append("- The false-UNREALs are dominated by "
                 + ", ".join(f"{d.split('/')[-1] or d}×{n}" for d, n in fu_fam.most_common(4))
                 + " — output-free assumption clusters synthesised standalone (TASKS.md gap #2).\n")

    L.append("\n### Verdict\n")
    if both:
        agg = sum_base / sum_ours if sum_ours else float("nan")
        agg_dir = "faster" if agg >= 1 else "slower"
        L.append(
            f"On the **median** OxiDD-contributing spec where both engines synthesize, tlsf-tools is "
            f"at **rough parity** ({med_ours*1000:.0f} ms vs {med_base*1000:.0f} ms). "
            f"In **aggregate we are ×{agg:.2f} {agg_dir}** than ltlsynt. "
            f"The genuine value is the **{len(ours_win_base_to)} specs ltlsynt cannot "
            f"synthesize in {args.timeout}s that we do** (GR(1) `amba_gr`, large decomposed safety). The "
            f"completeness blocker is **{len(less_complete)} specs ltlsynt solves that we don't** — now "
            f"dominated by **{len(false_unreal)} false-UNREALs** from output-free assumption clusters "
            f"(TASKS.md gap #2), not parse bugs.\n")
    L.append(SENTINEL_END + "\n")
    section = "".join(L)

    # Inject as a self-delimited section: replace between the sentinels if they
    # already exist (so reruns only touch this section), else append to the doc.
    existing = ""
    if os.path.exists(args.out):
        with open(args.out) as f:
            existing = f.read()
    if SENTINEL_START in existing and SENTINEL_END in existing:
        pre = existing[:existing.index(SENTINEL_START)]
        post = existing[existing.index(SENTINEL_END) + len(SENTINEL_END):]
        new = pre + section.rstrip("\n") + post
    elif existing.strip():
        new = existing.rstrip("\n") + "\n\n" + section
    else:
        new = "# BENCHGRAPH\n\n" + section
    with open(args.out, "w") as f:
        f.write(new)


if __name__ == "__main__":
    main()
