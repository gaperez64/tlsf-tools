#!/usr/bin/env python3
"""Benchmark tlsf-tools as a decomposition preprocessor for synthesis, against
standalone acacia-bonsai.

Two metrics, both rerunnable over the corpus and summarised into BENCHGRAPH.md:

  ROUTE SHAPE (whole corpus): how many residual clusters are solved in process,
  and what shape are the residual clusters that still need an external backend.

  SPEED: wall time of the wrapper pipeline (decompose + persistent OxiDD residual
  solving + external acacia-bonsai per remaining cluster + merge) vs a standalone
  acacia-bonsai baseline for the whole spec.  Solve STATUS is tracked too -- a
  fast failure is not a speedup, so speedups are only computed where both engines
  actually produced a controller.

Usage:
  scripts/benchgraph.py --corpus DIR --tlsfcompose PATH \
      [--solver PATH] [--out BENCHGRAPH.md] [--data data.tsv] \
      [--timeout 10] [--mem-gb 6] [--speed all|routed]
"""
import argparse
import csv
import itertools
import os
import re
import shutil
import signal
import statistics
import subprocess
import sys
import tempfile
import time
from datetime import datetime, timezone

# Per-child memory cap, enforced via a systemd transient scope (cgroup) -- set
# from --mem-gb in main().  RLIMIT_AS cannot be used: OxiDD reserves large
# virtual pools (>6 GB) even for tiny games, causing spurious SIGABRT panics; and
# RLIMIT_RSS is advisory-only on Linux.  A `systemd-run --user --scope` cgroup
# with MemoryMax + MemorySwapMax=0 gives a *hard* RSS+swap cap whose breach kills
# only that child's cgroup (verified), never the whole box -- which an unenforced
# cap previously let the OS OOM killer do.
MEM_GB = 6
FORCE_NO_CGROUP = False
SENTINEL_START = "<!-- BENCHGRAPH:PREPROCESSOR START (generated by scripts/benchgraph.py) -->"
SENTINEL_END = "<!-- BENCHGRAPH:PREPROCESSOR END -->"
ROW_FIELDS = [
    "name",
    "preprocessor_only",
    "oxidd_clusters",
    "external_clusters",
    "residual_class",
    "preproc_st",
    "preproc_t",
    "acacia_st",
    "acacia_t",
    "full_nodes",
    "external_nodes",
    "full_bytes",
    "external_bytes",
    "max_outputs_any_cluster",
    "max_outputs_external",
]
INT_FIELDS = [
    "oxidd_clusters",
    "external_clusters",
    "full_nodes",
    "external_nodes",
    "full_bytes",
    "external_bytes",
    "max_outputs_any_cluster",
    "max_outputs_external",
]
FLOAT_FIELDS = ["preproc_t", "acacia_t"]


def systemd_run_usable():
    if shutil.which("systemd-run") is None:
        return False
    try:
        proc = subprocess.run(
            ["systemd-run", "--user", "--scope", "--quiet", "--collect",
             "--", "true"],
            stdout=subprocess.DEVNULL,
            stderr=subprocess.DEVNULL,
            timeout=5,
            check=False,
        )
    except (OSError, subprocess.TimeoutExpired):
        return False
    return proc.returncode == 0


HAVE_SYSTEMD_RUN = systemd_run_usable()
SYSTEMD_SCOPE_COUNTER = itertools.count(1)


def _systemd_scope_name():
    return f"tlsfbench-{os.getpid()}-{next(SYSTEMD_SCOPE_COUNTER)}"


def _prepare_child_cmd(cmd):
    """Prepend a systemd transient-scope cgroup with a hard memory cap.  --quiet
    keeps the child's stderr clean (we parse it); --collect reaps the unit even
    on failure; MemorySwapMax=0 stops the child eating (zram) swap."""
    if FORCE_NO_CGROUP or not HAVE_SYSTEMD_RUN:
        return list(cmd), None
    unit = _systemd_scope_name()
    return ["systemd-run", "--user", "--scope", "--quiet", "--collect",
            "--unit", unit,
            "-p", f"MemoryMax={MEM_GB}G", "-p", "MemorySwapMax=0",
            "--"] + list(cmd), unit + ".scope"


def _cleanup_child_scope(p, unit):
    try:
        os.killpg(os.getpgid(p.pid), signal.SIGKILL)
    except ProcessLookupError:
        pass
    if not unit:
        return
    for cmd in (
        ["systemctl", "--user", "kill", "--signal=SIGKILL", unit],
        ["systemctl", "--user", "stop", unit],
    ):
        try:
            subprocess.run(
                cmd,
                stdout=subprocess.DEVNULL,
                stderr=subprocess.DEVNULL,
                timeout=5,
                check=False,
            )
        except (OSError, subprocess.TimeoutExpired):
            pass


def run_timed(cmd, timeout, env=None):
    """Run cmd with a wall-clock timeout and a hard memory cap (systemd cgroup),
    in its own session so the whole process group can be killed.  Returns
    (rc, stderr, secs, timed_out)."""
    start = time.monotonic()
    child_cmd, unit = _prepare_child_cmd(cmd)
    p = subprocess.Popen(child_cmd, stdout=subprocess.DEVNULL,
                         stderr=subprocess.PIPE,
                         env=env, start_new_session=True)
    try:
        _, err = p.communicate(timeout=timeout)
        return p.returncode, err.decode("utf-8", "replace"), time.monotonic() - start, False
    except subprocess.TimeoutExpired:
        _cleanup_child_scope(p, unit)
        try:
            _, err = p.communicate(timeout=5)
        except subprocess.TimeoutExpired:
            _cleanup_child_scope(p, unit)
            err = b""
        return 124, (err or b"").decode("utf-8", "replace"), time.monotonic() - start, True


def run_capture(cmd, timeout, env=None):
    """Run cmd with captured stdout/stderr, the same timeout and memory cap as
    run_timed.  Returns (rc, stdout, stderr, secs, timed_out)."""
    start = time.monotonic()
    child_cmd, unit = _prepare_child_cmd(cmd)
    p = subprocess.Popen(child_cmd, stdout=subprocess.PIPE,
                         stderr=subprocess.PIPE, env=env,
                         start_new_session=True)
    try:
        out, err = p.communicate(timeout=timeout)
        return (p.returncode, out.decode("utf-8", "replace"),
                err.decode("utf-8", "replace"), time.monotonic() - start,
                False)
    except subprocess.TimeoutExpired:
        _cleanup_child_scope(p, unit)
        try:
            out, err = p.communicate(timeout=5)
        except subprocess.TimeoutExpired:
            _cleanup_child_scope(p, unit)
            out, err = b"", b""
        return (124, (out or b"").decode("utf-8", "replace"),
                (err or b"").decode("utf-8", "replace"),
                time.monotonic() - start, True)


def compose_cmd(args, *parts):
    return [args.tlsfcompose, *parts, *args.compose_extra_arg]


